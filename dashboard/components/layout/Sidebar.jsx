// Top items shown in mobile bottom bar (max 5 for usability)
const MOBILE_NAV_ITEMS = NAV_ITEMS.slice(0, 5);

export default function Sidebar() {
  const pathname = usePathname();
  const [mobileOpen, setMobileOpen] = useState(false);
  const errorCount = useErrorCount();

  return (
    <>
      {/* Desktop sidebar */}
      <aside className={styles.sidebar}>
        <div className={styles.logo}>Stock Analysis</div>
        <nav className={styles.nav}>
          {NAV_ITEMS.map((item) => {
            const isActive =
              item.href === "/"
                ? pathname === "/"
                : pathname.startsWith(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`${styles.link}${isActive ? ` ${styles.active}` : ""}`}
              >
                {item.icon}
                {item.label}
                {item.href === "/errors" && errorCount > 0 && (
                  <span className={styles.errorBadge}>
                    {errorCount > 99 ? "99+" : errorCount}
                  </span>
                )}
              </Link>
            );
          })}
        </nav>
      </aside>

      {/* Mobile bottom bar */}
      <nav className={styles.mobileBar}>
        {MOBILE_NAV_ITEMS.map((item) => {
          const isActive =
            item.href === "/"
              ? pathname === "/"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`${styles.mobileLink}${isActive ? ` ${styles.mobileActive}` : ""}`}
            >
              {item.icon}
              <span>{item.label}</span>
            </Link>
          );
        })}
        <button
          className={`${styles.mobileLink}${mobileOpen ? ` ${styles.mobileActive}` : ""}`}
          onClick={() => setMobileOpen((v) => !v)}
        >
          <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
            <circle cx="12" cy="12" r="1" /><circle cx="12" cy="5" r="1" /><circle cx="12" cy="19" r="1" />
          </svg>
          <span>More</span>
        </button>
      </nav>

      {/* Mobile "More" drawer */}
      {mobileOpen && (
        <>
          <div className={styles.mobileOverlay} onClick={() => setMobileOpen(false)} />
          <div className={styles.mobileDrawer}>
            {NAV_ITEMS.slice(5).map((item) => {
              const isActive = pathname.startsWith(item.href);
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  className={`${styles.drawerLink}${isActive ? ` ${styles.active}` : ""}`}
                  onClick={() => setMobileOpen(false)}
                >
                  {item.icon}
                  {item.label}
                </Link>
              );
            })}
          </div>
        </>
      )}
    </>
  );
}
